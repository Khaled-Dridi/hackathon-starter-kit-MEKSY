#!/usr/bin/env node
/**
 * Pre-build step for Render (and any other static-site host).
 *
 * Reads the `API_URL` env var and writes it into `src/environments/environment.prod.ts`
 * so the Angular production build bakes in the right backend URL. Without
 * this, the frontend would call `/api` on its own domain, which doesn't
 * exist on Render's static-site CDN.
 *
 * The env var can be either:
 *   - A bare host (e.g. "charity-day-backend.onrender.com" — what Render's
 *     `fromService.property: host` returns). The script prepends "https://".
 *   - A full URL ("https://api.example.com"). Used as-is.
 *   - Unset → falls back to "/api" (local dev / docker-compose with nginx).
 *
 * Run before `ng build --configuration production`.
 */
const fs = require('fs');
const path = require('path');

const raw = (process.env.API_URL || '').trim();
let apiUrl;
if (!raw) {
  apiUrl = '/api';
} else if (raw.startsWith('http://') || raw.startsWith('https://') || raw.startsWith('/')) {
  apiUrl = raw.replace(/\/$/, ''); // strip trailing slash
} else {
  apiUrl = `https://${raw.replace(/\/$/, '')}`;
}

const outFile = path.join(__dirname, '..', 'src', 'environments', 'environment.prod.ts');
const contents = `// Generated by scripts/render-prebuild.js at build time.
// API_URL env var (or "/api" fallback for local docker-compose).
export const environment = {
  production: true,
  apiUrl: '${apiUrl}'
};
`;

fs.writeFileSync(outFile, contents);
console.log(`✓ environment.prod.ts written with apiUrl="${apiUrl}"`);

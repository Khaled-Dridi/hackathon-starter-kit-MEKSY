import { Injectable, computed, signal } from '@angular/core';

export type Lang = 'fr' | 'en';

const STORAGE_KEY = 'cd.lang';

/**
 * Lightweight signal-driven i18n. Components call {@link t} in templates;
 * the active language is held in a signal so re-renders happen automatically
 * when {@link setLang} flips it. Default is French (this is an internal
 * Inetum tool for the French Charity Day). The choice is persisted to
 * localStorage so a refresh doesn't reset it.
 *
 * <h3>Date formatting</h3>
 * The {@link locale} signal exposes the Angular locale id ("fr-FR" or "en-GB"),
 * which components pipe into {@code DatePipe} for locale-aware date rendering.
 */
@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly _lang = signal<Lang>(this.readInitial());
  readonly lang = this._lang.asReadonly();
  readonly locale = computed<string>(() => this._lang() === 'fr' ? 'fr-FR' : 'en-GB');

  setLang(l: Lang): void {
    this._lang.set(l);
    try { localStorage.setItem(STORAGE_KEY, l); } catch { /* private mode */ }
    // Apply on <html> so global selectors (e.g., screen reader, font fallback)
    // and CSS :lang() rules can react.
    document.documentElement.lang = l;
  }

  toggle(): void {
    this.setLang(this._lang() === 'fr' ? 'en' : 'fr');
  }

  /**
   * Look up a key in the active dictionary. Missing keys return the key
   * itself so a missing translation is loud (you'll see "actions.foo" on
   * screen) but never crashes the render.
   */
  t(key: string, params?: Record<string, string | number>): string {
    const dict = DICTS[this._lang()];
    let v = dict[key];
    if (v === undefined) {
      // Fall back to the other language so a missing entry in one lang
      // doesn't strand the UI in raw-key view.
      const other = this._lang() === 'fr' ? 'en' : 'fr';
      v = DICTS[other][key] ?? key;
    }
    if (params) {
      for (const k of Object.keys(params)) {
        v = v.replace(new RegExp(`\\{${k}\\}`, 'g'), String(params[k]));
      }
    }
    return v;
  }

  private readInitial(): Lang {
    try {
      const v = localStorage.getItem(STORAGE_KEY) as Lang | null;
      if (v === 'fr' || v === 'en') {
        document.documentElement.lang = v;
        return v;
      }
    } catch { /* ignore */ }
    document.documentElement.lang = 'fr';
    return 'fr';
  }
}

